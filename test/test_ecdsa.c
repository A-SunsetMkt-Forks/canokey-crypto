static void test_ecdsa_keygen(void **state) {
  (void) state;
  uint8_t priv_key[32], pub_key[64];
  uint8_t expected[] =
      {0x7A, 0x59, 0x31, 0x80, 0x86, 0x0C, 0x40, 0x37, 0xC8, 0x3C, 0x12, 0x74, 0x98, 0x45, 0xC8, 0xEE, 0x14, 0x24, 0xDD, 0x29, 0x7F, 0xAD, 0xCB, 0x89, 0x5E, 0x35, 0x82, 0x55, 0xD2, 0xC7, 0xD2, 0xB2,
      0xA8, 0xCA, 0x25, 0x58, 0x0F, 0x26, 0x26, 0xFE, 0x57, 0x90, 0x62, 0xFF, 0x1B, 0x99, 0xFF, 0x91, 0xC2, 0x4A, 0x0D, 0xA0, 0x6F, 0xB3, 0x2B, 0x5B, 0xE2, 0x01, 0x48, 0xC9, 0x24, 0x9F, 0x56, 0x50};
  ecdsa_generate(ECDSA_SECP256R1, priv_key, pub_key);
  for (int i = 0; i != 32; ++i) {
    // check magic number generated by mock random generator
    assert_int_equal(priv_key[i], i);
  }
  for (int i = 0; i != 64; ++i) {
    assert_int_equal(pub_key[i], expected[i]);
  }
}

